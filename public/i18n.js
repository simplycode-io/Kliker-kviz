const LANGUAGE_KEY = 'kviz-language';
const LANGUAGE_LOCALES = {
    sr: 'sr-RS',
    en: 'en-US',
    mk: 'mk-MK'
};

function getSavedLanguage() {
    const saved = localStorage.getItem(LANGUAGE_KEY);
    return saved && TRANSLATIONS[saved] ? saved : 'sr';
}

function getCurrentLanguage() {
    return getSavedLanguage();
}

function replaceVars(text, vars) {
    if (!vars) {
        return text;
    }
    return Object.keys(vars).reduce((result, key) => {
        return result.replace(new RegExp(`\\{${key}\\}`, 'g'), vars[key]);
    }, text);
}

function translate(key, vars = {}, lang = getCurrentLanguage()) {
    const locale = TRANSLATIONS[lang] || TRANSLATIONS.sr;
    const text = locale[key] || TRANSLATIONS.sr[key] || key;
    return replaceVars(text, vars);
}

function translatePage(lang = getCurrentLanguage()) {
    if (!TRANSLATIONS[lang]) {
        lang = 'sr';
    }
    localStorage.setItem(LANGUAGE_KEY, lang);
    document.documentElement.lang = lang;

    const titleEl = document.querySelector('title[data-i18n]');
    if (titleEl) {
        titleEl.textContent = translate(titleEl.dataset.i18n, {}, lang);
    }

    document.querySelectorAll('[data-i18n]').forEach((el) => {
        const key = el.dataset.i18n;
        if (key) {
            el.textContent = translate(key, {}, lang);
        }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
        const key = el.dataset.i18nPlaceholder;
        if (key) {
            el.placeholder = translate(key, {}, lang);
        }
    });

    document.querySelectorAll('[data-i18n-title]').forEach((el) => {
        const key = el.dataset.i18nTitle;
        if (key) {
            el.title = translate(key, {}, lang);
        }
    });

    document.querySelectorAll('[data-i18n-value]').forEach((el) => {
        const key = el.dataset.i18nValue;
        if (key) {
            el.value = translate(key, {}, lang);
        }
    });

    const select = document.getElementById('language-select');
    if (select) {
        select.value = lang;
    }
}

function initLanguageSelector() {
    const select = document.getElementById('language-select');
    if (!select) {
        return;
    }
    select.innerHTML = '';
    Object.entries(TRANSLATIONS).forEach(([lang, translation]) => {
        const option = document.createElement('option');
        option.value = lang;
        option.textContent = lang === 'sr' ? 'Srpski' : lang === 'en' ? 'English' : 'Македонски';
        select.appendChild(option);
    });
    select.value = getSavedLanguage();
    select.addEventListener('change', (event) => {
        translatePage(event.target.value);
        if (typeof window.onLanguageReady === 'function') {
            window.onLanguageReady(event.target.value);
        }
    });
}

function getLocale() {
    return LANGUAGE_LOCALES[getCurrentLanguage()] || 'sr-RS';
}

document.addEventListener('DOMContentLoaded', () => {
    initLanguageSelector();
    translatePage();
    if (typeof window.onLanguageReady === 'function') {
        window.onLanguageReady(getCurrentLanguage());
    }
});
